	<h1 id="pol-tica-de-privacidade-lei-geral-de-prote-o-de-dados-lgpd-">
		Política de Privacidade – Lei Geral de Proteção de Dados (LGPD)
	</h1>
	<p><em>Última Atualização: 22/03/2025</em></p>
	<h2 id="1-introdu-o">1. Introdução</h2>
	<p>
		Esta Política de Privacidade explica como a <strong>GLD Community</strong> coleta, utiliza e protege
		os dados pessoais dos usuários no Brasil, em conformidade com a Lei Geral de Proteção de Dados Pessoais
		(LGPD – Lei nº 13.709/2018). Se você reside no Brasil, possui direitos específicos sobre seus dados
		pessoais.
	</p>
	<h2 id="2-base-legal-para-tratamento-de-dados">2. Base Legal para Tratamento de Dados</h2>
	<p>Tratamos dados pessoais com base nos seguintes fundamentos legais:</p>
	<ul>
		<li>
			<strong>Consentimento:</strong> Quando você fornece permissão explícita para o processamento dos
			dados (exemplo: criação de conta, personalização de perfil).
		</li>
		<li>
			<strong>Legítimo Interesse:</strong> Para melhorar nossos serviços, garantir a segurança e manter
			a integridade da comunidade.
		</li>
		<li>
			<strong>Execução de Contrato:</strong> Para fornecer os recursos e funcionalidades da
			<strong>GLD Community</strong>.
		</li>
		<li><strong>Obrigação Legal:</strong> Para cumprir requisitos legais ou regulatórios.</li>
	</ul>
	<h2 id="3-dados-coletados">3. Dados Coletados</h2>
	<p>Coletamos os seguintes tipos de dados pessoais:</p>
	<ul>
		<li><strong>Identificadores:</strong> Nome de usuário, foto de perfil.</li>
		<li>
			<strong>Informações de Perfil:</strong> Descrição fornecida pelo usuário, lista de jogos favoritos,
			conquistas.
		</li>
		<li>
			<strong>Dados de Uso:</strong> Interações dentro do aplicativo, estatísticas anonimizadas.
		</li>
	</ul>
	<p>
		Não vendemos dados de usuários e não compartilhamos informações com terceiros para fins
		publicitários.
	</p>
	<h2 id="4-direitos-do-titular-de-dados">4. Direitos do Titular de Dados</h2>
	<p>De acordo com a LGPD, você tem os seguintes direitos:</p>
	<h3 id="4-1-direito-de-acesso">4.1 Direito de Acesso</h3>
	<p>Solicitar detalhes sobre os dados pessoais que armazenamos sobre você.</p>
	<h3 id="4-2-direito-de-corre-o">4.2 Direito de Correção</h3>
	<p>Solicitar a atualização ou correção de dados imprecisos.</p>
	<h3 id="4-3-direito-de-exclus-o">4.3 Direito de Exclusão</h3>
	<p>Solicitar a exclusão de seus dados pessoais, sujeito a obrigações legais.</p>
	<h3 id="4-4-direito-de-restri-o-de-processamento">4.4 Direito de Restrição de Processamento</h3>
	<p>Solicitar a limitação do uso de seus dados em determinadas situações.</p>
	<h3 id="4-5-direito-portabilidade">4.5 Direito à Portabilidade</h3>
	<p>Solicitar a transferência dos seus dados para outro serviço em formato estruturado.</p>
	<h3 id="4-6-direito-de-revogar-o-consentimento">4.6 Direito de Revogar o Consentimento</h3>
	<p>
		Quando o tratamento dos dados for baseado em consentimento, você pode revogá-lo a qualquer
		momento.
	</p>
	<h2 id="5-seguran-a-e-reten-o-de-dados">5. Segurança e Retenção de Dados</h2>
	<p>
		Adotamos medidas de segurança para proteger seus dados pessoais contra acessos não autorizados,
		perda ou alteração. Os dados são armazenados apenas pelo tempo necessário para cumprir sua
		finalidade ou exigências legais.
	</p>
	<h2 id="6-como-exercitar-seus-direitos">6. Como Exercitar Seus Direitos</h2>
	<p>
		Para exercer seus direitos sob a LGPD, entre em contato conosco pelo e-mail <strong
			>gld.community.sup@gmail.com</strong
		>. Podemos solicitar a verificação da sua identidade antes de processar a solicitação.
	</p>
	<h2 id="7-altera-es-a-esta-pol-tica">7. Alterações a Esta Política</h2>
	<p>
		Podemos atualizar esta Política de Privacidade periodicamente. O uso contínuo da <strong
			>GLD Community</strong
		> após quaisquer alterações significa que você aceita os novos termos.
	</p>
	<p>
		Se tiver dúvidas, entre em contato pelo e-mail <strong>gld.community.sup@gmail.com</strong>.
	</p>
